    //Các method xử lý các sự kiện thêm, xóa, sửa, nhập mới và thoát chương trình
    private void addSinhVien() {
        SinhVien sv = new SinhVien();
        sv.setName(txthoten.getText());
        sv.setDiem(Double.parseDouble(txtdiem.getText()));
        sv.setNganh((String) cbnganh.getSelectedItem());
        list.add(sv);
        txthocluc.setText(sv.getGrade());
        ckthuong.setSelected(sv.isBonus());
    }

    private void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tablethongtin.getModel();
        model.setRowCount(0);

        for (SinhVien sv : list) {
            Object[] row = new Object[]{sv.getName(), sv.getDiem(), sv.getNganh(), sv.getGrade(), sv.isBonus()};
            model.addRow(row);
        }
    }

    private void removeSinhVien() {
        int index = tablethongtin.getSelectedRow();
        list.remove(index);
        txthoten.setText("");
        txtdiem.setText("");
        txthocluc.setText("");
        cbnganh.setSelectedIndex(0);
        ckthuong.setSelected(false);
    }

    private void updateSinhVien() {
        int index = tablethongtin.getSelectedRow();
        SinhVien sv = list.get(index);

        sv.setName(txthoten.getText());
        sv.setDiem(Double.parseDouble(txtdiem.getText()));
        sv.setNganh((String) cbnganh.getSelectedItem());
        txthocluc.setText(sv.getGrade());
        ckthuong.setSelected(sv.isBonus());
    }

    private void newInsertSinhVien() {
        txthoten.setText("");
        txtdiem.setText("");
        txthocluc.setText("");
        cbnganh.setSelectedIndex(0);
        ckthuong.setSelected(false);
        list.clear();
    }

    //Sắp xếp danh sách trong table theo tên hoặc điểm.
    private void orderbyDiem() {
        Comparator<SinhVien> com = new Comparator<SinhVien>() {
            @Override
            public int compare(SinhVien o1, SinhVien o2) {
                Double d1 = o1.getDiem();
                Double d2 = o2.getDiem();
                return d1.compareTo(d2);
            }
        };
        Collections.sort(list, com);
    }

    private void orderbyTen() {
        Comparator<SinhVien> com = new Comparator<SinhVien>() {
            @Override
            public int compare(SinhVien o1, SinhVien o2) {
                return o1.getName().compareTo(o2.getName());
            }
        };
        Collections.sort(list, com);
    }
