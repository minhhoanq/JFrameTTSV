    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        if (Double.parseDouble(txtdiem.getText()) > 10) {
            txtdiem.setText("");
        }
        if (txtdiem.getText().isEmpty() || txthoten.getText().isEmpty()) {
            if (txthoten.getText().isEmpty()) {
                txthoten.setBackground(Color.red);
            } else {
                txthoten.setBackground(Color.white);
            }
            if (txtdiem.getText().isEmpty()) {
                txtdiem.setBackground(Color.red);
            } else {
                txtdiem.setBackground(Color.white);
            }
            JOptionPane.showMessageDialog(this, "Không được để trống!", "", JOptionPane.ERROR_MESSAGE);
        } else {
            txthoten.setBackground(Color.white);
            txtdiem.setBackground(Color.white);
            addSinhVien();
            fillToTable();
        }
    }//GEN-LAST:event_btnthemActionPerformed

